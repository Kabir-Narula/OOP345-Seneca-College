/*
An lvalue, short for "locator value," represents an expression that refers to a memory location. It typically corresponds to a variable or an object that can be assigned a new value.
Lvalues can appear on both the left and right sides of an assignment operator (=).
Examples of lvalues include variables, array elements, and non-const objects.
Variables: int x;
Array elements: arr[2];
Non-const objects: myObject;
References: int& ref = x; */

int x = 10;  // 'x' is an lvalue
int arr[5];  // 'arr[2]' is an lvalue


